<?php
	class ItemTypeModel extends CI_Model {
	
	var $Type_Code;
	var $Item_Type;
	var $Category_Code;
    var $Bulk_Code;
    var $Unit_Code;
    var $Description;
	
	function __construct()
	{
		// Call the Model constructor
		parent::__construct();
	}//constructor
	
	function getType_Code() { return $this->Type_Code; } 
    function getItem_Type() { return $this->Item_Type; } 
    function getCategory_Code() { return $this->Category_Code; } 
    function getBulk_Code() { return $this->Bulk_Code; } 
    function getUnit_Code() { return $this->Unit_Code; } 
    function getDescription() { return $this->Description; } 
    
    function setType_Code($x) { $this->Type_Code = $x; } 
    function setItem_Type($x) { $this->Item_Type = $x; } 
    function setCategory_Code($x) { $this->Category_Code = $x; } 
    function setBulk_Code($x) { $this->Bulk_Code = $x; } 
    function setUnit_Code($x) { $this->Unit_Code = $x; } 
    function setDescription($x) { $this->Description = $x; }
}//class
?>